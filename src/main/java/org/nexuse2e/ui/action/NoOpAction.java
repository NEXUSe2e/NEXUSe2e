package org.nexuse2e.ui.action;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;
import org.apache.struts.action.ActionMessages;

/**
 * This action performs no operation.
 * This is useful for instance to determines whether to show the
 * welcome screen or whether to forward to the login screen.
 * This login forward decison will be made by the super class,
 * before this @{link executeNexusE2EAction(ActionMapping, ActionForm, HttpServletRequest, HttpServletResponse, ActionMessages, ActionMessages )}
 * is called. That's why this action always forwards to "sucess". 
 * @author mbreilmann, s_schulze
 *
 */
public class NoOpAction extends NexusE2EAction {

    /* (non-Javadoc)
     * @see org.nexuse2e.ui.action.NexusE2EAction#executeNexusE2EAction(org.apache.struts.action.ActionMapping, org.apache.struts.action.ActionForm, javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse, org.apache.struts.action.ActionMessages, org.apache.struts.action.ActionMessages)
     */
    @Override
    public ActionForward executeNexusE2EAction( ActionMapping actionMapping, ActionForm actionForm,
            HttpServletRequest request, HttpServletResponse response, ActionMessages errors, ActionMessages messages )
            throws Exception {

        ActionForward success = actionMapping.findForward( ACTION_FORWARD_SUCCESS );

        return success;
    }

}
